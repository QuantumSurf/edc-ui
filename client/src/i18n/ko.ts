// Use a deep string type instead of literal types for cross-locale compatibility
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends (...args: infer A) => string
      ? (...args: A) => string
      : DeepStringify<T[K]>;
};

export type Translations = DeepStringify<typeof ko>;
export default ko as Translations;
